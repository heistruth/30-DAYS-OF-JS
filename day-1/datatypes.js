// datatypes.js

// Declare variables with different data types
let stringVariable = "Hello, world!"; // string data type
let numberVariable = 42; // number data type
let booleanVariable = true; // boolean data type
let arrayVariable = [1, 2, 3]; // array data type
let objectVariable = { key: "value" }; // object data type
let undefinedVariable; // undefined data type
let nullVariable = null; // null data type

// Use typeof operator to check the data type of each variable
console.log(typeof stringVariable);
console.log(typeof numberVariable);
console.log(typeof booleanVariable);
console.log(typeof arrayVariable);
console.log(typeof objectVariable);
console.log(typeof undefinedVariable);
console.log(typeof nullVariable);
